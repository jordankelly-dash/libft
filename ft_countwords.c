#include "libft.h"

int		ft_countwords(char const *s, char c)
{
	int i;
	int a;

	i = 0;
	a = 0;
	while (s[a])
	{
		while (s[a] == c)
			a++;
		if (s[a] != c && s[a] != '\0')
			i++;
		while (s[a] != c && s[a] != '\0')
			a++;
	}
	return (i);
}
